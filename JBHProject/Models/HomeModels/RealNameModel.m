#import "RealNameModel.h"

@implementation RealNameModel
+ (instancetype)RealNameModelWith:(NSString *)contentStr
                 iconImageNameStr:(NSString *)iconImageNameStr
                 pushImageNameStr:(NSString *)pushImageNameStr
{
    return [[self alloc]initWithModel:contentStr iconImageNameStr:iconImageNameStr pushImageNameStr:pushImageNameStr
         ];
}
- (id)initWithModel:(NSString *)contentStr iconImageNameStr:(NSString *)iconImageNameStr
        pushImageNameStr:(NSString *)pushImageNameStr
{
    if (self = [super init]) {
        _contentStr = contentStr;
        _iconImageNameStr = iconImageNameStr;
        _pushImageNameStr = pushImageNameStr;
    }
    return self;
}

@end
